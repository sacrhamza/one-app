#include "libft.h"

char	*ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;

	if (s == 0 || f == 0)
		return (0);
	i = 0;
	while (s[i] != '\0')
	{
		f(i, s+ i);
		i++;
	}
	return (s);
}
