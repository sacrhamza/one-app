#include "libft.h"

int	ft_word_count(char const *s, char c)
{
	int		i;
	size_t	j;

	j = 0;
	i = 0;
	while (*(s + j) != 0)
	{
		if (*(s + j) != c && (*(s + 1 + j) == c || *(s + j + 1) == 0))
			i++;
		j++;
	}
	return (i);
}
