#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	number;

	number = n;
	if (number < 0)
	{
		write(fd, "-", 1);
		number *= -1;
	}
	if (number >= 10)
	{
		ft_putnbr_fd(number / 10, fd);
		number = number % 10 + '0';
		write(fd, &number, 1);
	}
	else
	{
		number = number + '0';
		write(fd, &number, 1);
	}
}
